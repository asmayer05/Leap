def binToBraille(bin):
	i = 0
	out = ""
	while(i <= len(bin) - 6):
		try:
			while ("\n" in bin[i:i+6]):
				out += "\n"
				i += 1
			out += lib[bin[i:i+6]]
		except:
			pass
		i += 6
	return out